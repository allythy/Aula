package pkgwhile;

public class While {

    public static void main(String[] args) {
        
        int i = 1; // Declarando a variavel contador
        while (i < 5) {  /// enquanto o valor de i for menor que 5 vai executar o bloco de instrução 
            System.out.println("Contando: " + i); // imprimi o valor na tela
             i++; // incrementar uma unidade
        }
    }

}
