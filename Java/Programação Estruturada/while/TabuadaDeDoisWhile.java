
package tabuadadedoiswhile;


public class TabuadaDeDoisWhile {

    
    public static void main(String[] args) {
        
        int contador = 0;
        while(contador <= 10)
        {
            contador++;
            System.out.println("2 x " + contador + "= " + 2 * contador);
        }
 
    }
    
}
